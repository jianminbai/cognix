第三个误区，是只在抽象层面理解它，而不让它改变具体行为。认知如果不能进入行动，就会变成智识装饰。真正理解一个概念，至少应该改变三个东西：你记录什么，你复盘什么，你下注什么。你记录的东西说明你认为哪些变量重要；你复盘的方式说明你如何理解因果；你下注的方向说明你真正相信什么。

